    //Validar que el correo tenga @, _, letras y dígitos, y que no sea mayor a 25 caracteres
    private void txtCorreoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCorreoKeyTyped
        char validacion = evt.getKeyChar();

        if (txtCorreo.getText().length() > 25 || ((!Character.isLowerCase(validacion) && !(Character.isDigit(validacion))) && validacion != '@' && validacion != '_')
            && validacion != '.')
        evt.consume();
    }//GEN-LAST:event_txtCorreoKeyTyped
    //Modificar al cliente
    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        //Si los campos nombre y teléfono están rellenados
        if (txtNombre.getText().length() > 0 && txtTelefono.getText().length() > 0)
        {//Si el correo, el cumpleaños y el nit están bien ingresados
            if (correoBienIngresado() && cumpleaniosBienIngresado() && (nitBienIngresado() || txtNit.getText().equals("c/f")))//Validar si el correo o el cumpleaños están bien ingresados
            {
                //Realizar request post
                List<String> parametros = new ArrayList<String>();
                asignarParametros(parametros);
                Integer id = this.id;
                requests.put(parametros, id);
                JOptionPane.showMessageDialog(null, "El cliente ha sido modificado con éxito");
                this.setVisible(false);//Cerrar el form
            }
            else//Si no, indicar qué no se ha ingresado bien
            {
                if (!correoBienIngresado())
                JOptionPane.showMessageDialog(null, "El correo no está bien ingresado");
                else if (!cumpleaniosBienIngresado())
                JOptionPane.showMessageDialog(null, "El cumpleaños no está bien ingresado");
                else if (!nitBienIngresado())
                JOptionPane.showMessageDialog(null, "El nit no está bien ingresado");
                else if (!correoBienIngresado() && !cumpleaniosBienIngresado())
                JOptionPane.showMessageDialog(null, "El correo y el cumpleaños no están bien ingresados");
                else if (!correoBienIngresado() && !nitBienIngresado())
                JOptionPane.showMessageDialog(null, "El correo y el nit no están bien ingresados");
                else if (!cumpleaniosBienIngresado() && nitBienIngresado())
                JOptionPane.showMessageDialog(null, "El cumpleaños y el nit no están bien ingresados");
                else if (!cumpleaniosBienIngresado() && !nitBienIngresado() && !correoBienIngresado())
                JOptionPane.showMessageDialog(null, "El correo, el nit y el cumpleaños no están bien ingresados");
            }
        }
        else
        JOptionPane.showMessageDialog(null, "Los campos nombre y teléfono no pueden estar vacíos");
    }//GEN-LAST:event_btnEnviarActionPerformed
