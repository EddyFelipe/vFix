    //Validar que el nombre tenga sólo letras y espacios, y validar que tenga una longitud menor o igual a 55 caracteres
    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char validacion = evt.getKeyChar();

        if (txtNombre.getText().length() > 55 || (!Character.isLetter(validacion) && validacion != ' '))
        evt.consume();
    }//GEN-LAST:event_txtNombreKeyTyped
    //Validar que el teléfono sólo tenga + y números, y que tenga una longitud menor o igual a 16 caracteres
    private void txtTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoKeyTyped
        char validacion = evt.getKeyChar();

        if (txtTelefono.getText().length() > 16 || (!Character.isDigit(validacion) && validacion != '+' && validacion != '-'))
        evt.consume();
    }//GEN-LAST:event_txtTelefonoKeyTyped
    //Validar que la fecha de nacimiento tenga - y números, y que no sea mayor a 11 caracteres
