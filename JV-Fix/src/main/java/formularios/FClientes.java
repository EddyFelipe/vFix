    private void cargarInformacion()
    {
        Object O[] = null;
        listaClientes = (List<Cliente>)requestGet.get("http://localhost:8000/api/clientes/");
        for (int i = 0; i < listaClientes.size(); i++)
        {
            clientes.addRow(O);
            int estrellas = listaClientes.get(i).getEstrellas();
            clientes.setValueAt(listaClientes.get(i).getCodigo(), i, 0);
            clientes.setValueAt(listaClientes.get(i).getNombre(), i, 1);
            clientes.setValueAt(listaClientes.get(i).getTelefono(), i, 2);
            clientes.setValueAt(listaClientes.get(i).getNit(), i, 3);
            clientes.setValueAt(listaClientes.get(i).getDireccion(), i, 4);
            clientes.setValueAt(listaClientes.get(i).getCumpleanos(), i, 5);
            if (estrellas == 1)
                clientes.setValueAt("Muy poca relevancia", i, 6);
            else if (estrellas == 2)
                clientes.setValueAt("Poca relevancia", i, 6);
            else if (estrellas == 3)
                clientes.setValueAt("Más o menos relevante", i, 6);
            else if (estrellas == 4)
                clientes.setValueAt("Mucha relevancia", i, 6);
            else if (estrellas == 5)
                clientes.setValueAt("Bastante relevancia", i, 6);
            clientes.setValueAt(listaClientes.get(i).getCorreo(), i, 7);
        }
    }
    
