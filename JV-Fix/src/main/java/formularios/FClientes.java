    private void decidirCategoriaSeleccionada()
    {
        for (int i = 1; i < cbModRelevancia.getItemCount(); i++)
        {
            if (String.valueOf(tClientes.getValueAt(tClientes.getSelectedRow(), 6))
                    .equals(String.valueOf(cbModRelevancia.getItemAt(i))))
                cbModRelevancia.setSelectedIndex(i);
        }
    }
    
